import math
lado_quadrado = float(input("Diga o valor do lado do quadrado:"))
perimetro = (lado_quadrado * 4)
área = (lado_quadrado * 2)
diagonal = (math.sqrt(2)) * lado_quadrado
print("O perimetro do quadrado é:",perimetro)
print("A área do quadrado é:", área)
print("A diagonal do quadrado é:", diagonal)
